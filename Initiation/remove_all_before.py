from typing import Iterable


def remove_all_before(items: list, border: int) -> Iterable:

    if not len(items) == 0:
        for num in items:
            if (num == border):
                for i in items:
                    if not items[0] == border:
                        del items[0]
                    elif items[0] == border:
                        break
                return items
        if not (num == border):
            return items
    else:
        return items


